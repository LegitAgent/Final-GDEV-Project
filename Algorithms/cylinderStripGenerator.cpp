float cylinderStrip[sizeof(topCircle) / sizeof(float) * 2];

// This function is to generate the triangle strip between two circles
// Useful for generating cylinders
float* generateCylinderStrip(const float* topCircle, const float* bottomCircle,
                            int size, float* cylinderStrip) {
    int idx = 0;
    for (int i = TOTAL_VECTOR_POINTS; i < size; i += TOTAL_VECTOR_POINTS) {
        std::copy(topCircle + i, topCircle + i + TOTAL_VECTOR_POINTS, cylinderStrip + idx);
        idx += TOTAL_VECTOR_POINTS;
        std::copy(bottomCircle + i, bottomCircle + i + TOTAL_VECTOR_POINTS, cylinderStrip + idx);
        idx += TOTAL_VECTOR_POINTS;
    }
    // close the cylinder by repeating the first rim pair
    std::copy(topCircle + TOTAL_VECTOR_POINTS, topCircle + TOTAL_VECTOR_POINTS * 2, cylinderStrip + idx);
    idx += TOTAL_VECTOR_POINTS;
    std::copy(bottomCircle + TOTAL_VECTOR_POINTS, bottomCircle + TOTAL_VECTOR_POINTS * 2, cylinderStrip + idx);
    return cylinderStrip;
}

int main() {
    int stripSize = sizeof(topCircle) / sizeof(float) * 2;
    generateCylinderStrip(topCircle, bottomCircle, sizeof(topCircle) / sizeof(float), cylinderStrip);
    for (int i = 0; i < stripSize; i++) {
        std::cout << std::fixed << std::setprecision(6) << cylinderStrip[i] << "f, ";
        if ((i + 1) % TOTAL_VECTOR_POINTS == 0) std::cout << "\n";
    }
}